namespace Geocrest.Model.ArcGIS.Tasks
{
    /// <summary>
    /// Screen image display parameters
    /// </summary>
    public class ImageDisplayParameters
    {
        /// <summary>
        /// Gets or sets the width.
        /// </summary>
        /// <value>
        /// The width.
        /// </value>
        public int Width { get; set; }

        /// <summary>
        /// Gets or sets the height.
        /// </summary>
        /// <value>
        /// The height.
        /// </value>
        public int Height { get; set; }

        /// <summary>
        /// Gets or sets the DPI.
        /// </summary>
        /// <value>
        /// The DPI.
        /// </value>
        public int DPI { get; set; }
    }
}
